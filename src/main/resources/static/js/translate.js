function ja(en) {
    var ret = "";
    switch (en) {
        case "cocoa":
        ret = "ココア";
        break;
        case "sugar":
        ret = "砂糖";
        break;
        case "cinnammon":
        ret = "シナモン";
        break;
        case "saltAndPepper":
        ret = "塩コショウ";
        break;
        case "kinako":
        ret = "キナコ";
        break;
    }
    return ret;
}